#include <stdio.h>
/**
 * add - adds
 * @a: par
 * @b: par
 * Description: adds two integers
 * Return: sum
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts
 * @a: parameter
 * @b: parameter
 * Description: subtracts an int from another
 * Return: result of sub
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies
 * @a: parameter
 * @b: parameter
 * Description: multiplies two integers
 * Return: result
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - division
 * @a: parameter
 * @b: parameter
 * Description: divides two integers
 * Return: result
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modulus
 * @a: parameter
 * @b: parameter
 * Description: gives modulus of a integer
 * Return: result
 */
int mod(int a, int b)
{
	return (a % b);
}
